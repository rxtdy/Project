DECLARE @i INT = 1;

WHILE @i <= 100
BEGIN
    INSERT INTO dbo.Все_Группы (
        Название, Учебный_План, Код_Специальности, Курс, Код_Факультета,
        УчебныйГод, Направление, Специальность, Специализации,
        Код_Подразделения, Удалена, СрокОбучения, Уровень, Форма_Обучения)
    SELECT 
        CASE 
            WHEN s.Код <= 2 THEN CONCAT(N'ФИ-', RIGHT(100+@i,2))
            WHEN s.Код BETWEEN 3 AND 4 THEN CONCAT(N'ФЭ-', RIGHT(100+@i,2))
            WHEN s.Код = 5 THEN CONCAT(N'ФМ-', RIGHT(100+@i,2))
            WHEN s.Код BETWEEN 6 AND 7 THEN CONCAT(N'ФГН-', RIGHT(100+@i,2))
            ELSE CONCAT(N'ФФ-', RIGHT(100+@i,2))
        END AS Название,
        CONCAT(N'План_', s.ОКСО, '_', @i),
        s.Код AS Код_Специальности,
        ((@i-1)%6)+1 AS Курс,
        s.Код_Факультета,
        CASE (s.Код - 1) % 3
            WHEN 0 THEN N'2023-2024'
            WHEN 1 THEN N'2024-2025'
            WHEN 2 THEN N'2025-2026'
        END AS УчебныйГод,
        s.Название AS Направление,
        s.Специальность,
        s.Название_Спец AS Специализации,
        s.Код_Факультета,
        0,
        s.Срок_Обучения,
        CASE 
            WHEN s.Квалификация=N'Бакалавр' THEN 1 
            WHEN s.Квалификация=N'Специалист' THEN 2 
            WHEN s.Квалификация=N'Магистр' THEN 3 
            WHEN s.Квалификация=N'Аспирант' THEN 4 
            ELSE 5
        END,
        s.КодФормыОбучения
    FROM dbo.Специальности s
    WHERE s.Код = ((@i-1)%8)+1;
    SET @i += 1;
END

